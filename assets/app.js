function fillContactCard(id, label, value, href) {
  const node = document.querySelector(id);
  node.href = href;
  node.innerHTML = `
    <p class="text-sm text-slate-400">${label}</p>
    <p class="mt-2 break-all text-base leading-7 text-white">${value}</p>
  `;
}

function renderNav(localeData) {
  navLinks.forEach((link, index) => {
    link.textContent = localeData.nav[index];
  });
}

function renderPage() {
  const localeData = profileData[state.locale];
  document.documentElement.lang = state.locale === "zh" ? "zh-CN" : "en";
  document.title = localeData.meta.title;
  document.querySelector('meta[name="description"]').setAttribute("content", localeData.meta.description);

  Object.entries(localeData.hero).forEach(([key, value]) => setText(`hero.${key}`, value));
  Object.entries(localeData.about).forEach(([key, value]) => setText(`about.${key}`, value));
  Object.entries(localeData.education).forEach(([key, value]) => setText(`education.${key}`, value));
  Object.entries(localeData.skills).forEach(([key, value]) => setText(`skills.${key}`, value));
  Object.entries(localeData.projects).forEach(([key, value]) => setText(`projects.${key}`, value));
  Object.entries(localeData.contact).forEach(([key, value]) => setText(`contact.${key}`, value));
  renderNav(localeData);

  educationList.innerHTML = "";
  localeData.educationList.forEach((item) => educationList.appendChild(createEducationItem(item, localeData.education)));

  hardSkills.innerHTML = "";
  localeData.skillItems.hard.forEach((item) => hardSkills.appendChild(createSkillItem(item, localeData.skills.levelLabel)));

  softSkills.innerHTML = "";
  localeData.skillItems.soft.forEach((item) => softSkills.appendChild(createSkillItem(item, localeData.skills.levelLabel)));

  projectsList.innerHTML = "";
  localeData.projectsList.forEach((item) => projectsList.appendChild(createProjectItem(item, localeData.projects)));

  fillContactCard("#contact-email", localeData.contact.emailLabel, localeData.contactInfo.email, `mailto:${localeData.contactInfo.email}`);
  fillContactCard("#contact-phone", localeData.contact.phoneLabel, localeData.contactInfo.phone, `tel:${localeData.contactInfo.phone}`);
  fillContactCard("#contact-github", localeData.contact.githubLabel, localeData.contactInfo.github, localeData.contactInfo.github);
  fillContactCard("#contact-portfolio", localeData.contact.portfolioLabel, localeData.contactInfo.portfolio, localeData.contactInfo.portfolio);
  languageToggle.textContent = state.locale === "zh" ? "EN" : "中文";
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("reveal-in");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

revealItems.forEach((item) => revealObserver.observe(item));

const progressObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        progressObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.55 }
);

function observeProgressBars() {
  document.querySelectorAll("[data-progress-fill]").forEach((bar) => progressObserver.observe(bar));
}

const navObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      const link = navLinks.find((item) => item.getAttribute("href") === `#${entry.target.id}`);
      if (!link) return;
      if (entry.isIntersecting) {
        navLinks.forEach((item) => item.classList.remove("is-active"));
        link.classList.add("is-active");
      }
    });
  },
  { rootMargin: "-45% 0px -45% 0px" }
);

sections.forEach((section) => navObserver.observe(section));

languageToggle.addEventListener("click", () => {
  state.locale = state.locale === "zh" ? "en" : "zh";
  renderPage();
  observeProgressBars();
});

renderPage();
observeProgressBars();
